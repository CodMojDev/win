class NtlCode(Code):
    def add_import(self, storage_import: str, import_type: str = '*'):
        assert self.file and not self.file.closed
        
        if import_type is not None:
            self.file.write(f'from {storage_import} import {import_type}\n')
        else:
            if storage_import.startswith('.'):
                self.file.write(f'from . import {storage_import[1:]}\n')
            else:
                self.file.write(f'import {storage_import}\n')
    
    def append_indented(self, string: str):
        assert self.file and not self.file.closed
            
        self.file.write(self.make_indents() + string + '\n')

def NtlGenInit():
    global MSCORLIB, ATTR, METHINFO, PARAINFO
    if not NtlIsRunning():
        if NtlExtGetInitState() == NtlInitState.Failure:
            raise RuntimeError('Previously CLR initialization failed, the CLR will not initialized again.')
        NtlInitCLR()
        MSCORLIB = NtlLoadMscorlib()
    else:
        MSCORLIB = NtlExtGetMscorlib()
    METHINFO = MSCORLIB.System.Reflection.MethodInfo._type
    PARAINFO = MSCORLIB.System.Reflection.ParameterInfo._type
    ATTR = MSCORLIB.System.Attribute
            
